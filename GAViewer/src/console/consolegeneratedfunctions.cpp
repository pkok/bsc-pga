	p3ga o = arg[1]->p();
	
	int vgrade = v.largestGrade() & 1;

	if (vgrade)	 return new consoleVariable("", p3ga(v.inverse() * o.gradeInvolution() * v));
	else return new consoleVariable("", p3ga(v.inverse() * o * v));
